-- User-facing declension type "-" (old-style "ъ");
-- mapped to "-normal" (old-style "ъ-normal") or "-sib" (old-style "ъ-sib")
detect_decl_old["ъ"] = function(stem, stress)
	if sibilant_suffixes[ulower(usub(stem, -1))] then
